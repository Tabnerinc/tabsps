package com.h2o.ps360.dataobjects.ui.service.dataobjects;

public class SignupResponseObject {
public String defaultdomaincookie;
public Integer    patientid;
public int    httpresponse;
public String responsedescription;
public String getDefaultdomaincookie() {
	return defaultdomaincookie;
}
public void setDefaultdomaincookie(String defaultdomaincookie) {
	this.defaultdomaincookie = defaultdomaincookie;
}

public Integer getPatientid() {
	return patientid;
}
public void setPatientid(Integer patientid) {
	this.patientid = patientid;
}
public int getHttpresponse() {
	return httpresponse;
}
public void setHttpresponse(int httpresponse) {
	this.httpresponse = httpresponse;
}
public String getResponsedescription() {
	return responsedescription;
}
public void setResponsedescription(String responsedescription) {
	this.responsedescription = responsedescription;
}

}
